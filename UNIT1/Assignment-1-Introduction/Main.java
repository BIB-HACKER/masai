class Main{
    public static void main(String[] args) {
        // Define a variable called number of int data type
        int number;

        // Initialize the number with value 10
        number = 10;

        // Print the value stored in the variable number
        System.out.println("Initial value of number: " + number);

        // Assign the same variable number with 20
        number = 20;

        // Print the value stored in the variable number
        System.out.println("New value of number: " + number);
    }
}
